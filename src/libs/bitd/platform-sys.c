/*
 *============================================================================
 *                        bitd_sys_init
 *============================================================================
 * Description:     
 * Parameters:    
 * Returns:  
 */
bitd_boolean bitd_sys_init(void) {

#if defined(_WIN32) || defined(__CYGWIN__)
    /* Enable auto-flush for stdout and stderr on Windows - so printf()
       displays the output immediately */
    setvbuf(stdout, NULL, _IONBF, 0);
    setvbuf(stderr, NULL, _IONBF, 0);
#endif

    return bitd_sys_thread_init() &&
        bitd_sys_socket_init();
} 



/*
 *============================================================================
 *                        bitd_sys_destroy
 *============================================================================
 * Description:     
 * Parameters:    
 * Returns:  
 */
void bitd_sys_deinit(void) {
    bitd_sys_socket_deinit();
    bitd_sys_thread_deinit();
} 

