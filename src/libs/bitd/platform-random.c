/*
 *============================================================================
 *                        bitd_random
 *============================================================================
 * Description:     
 * Parameters:    
 * Returns:  
 */
bitd_uint32 bitd_random(void) {
    return bitd_arch_random();
} 


/*
 *============================================================================
 *                        bitd_srandom
 *============================================================================
 * Description:     
 * Parameters:    
 * Returns:  
 */
void bitd_srandom(bitd_int32 seed) {
    bitd_arch_srandom(seed);
} 


/*
 *============================================================================
 *                        bitd_random64
 *============================================================================
 * Description:     
 * Parameters:    
 * Returns:  
 */
bitd_uint64 bitd_random64(void) {
    return (((bitd_uint64)bitd_arch_random())<<32) + 
	(bitd_uint64)bitd_arch_random();
} 



