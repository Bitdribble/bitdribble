/*
 *============================================================================
 *                        mmr_log
 *============================================================================
 * Description:     
 * Parameters:    
 * Returns:  
 */
int mmr_log(ttlog_level level, char *format_string, ...) {
    va_list args;
    int ret;

    va_start(args, format_string);
    ret = mmr_vlog(level, format_string, args);
    va_end(args);

    return ret;        
} 


/*
 *============================================================================
 *                        mmr_vlog
 *============================================================================
 * Description:     
 * Parameters:    
 * Returns:  
 */
int mmr_vlog(ttlog_level level, char *format_string, va_list args) {

    if (g_mmr_cb && g_mmr_cb->vlog) {
	return g_mmr_cb->vlog(level, format_string, args);
    }

    return 0;
} 
